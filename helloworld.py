print(y / x)                    # division(/)
print(x ** y)                   # exponential(**)
print(y ** x)                   # exponential(**)
print(x % y)                    # modulus(%)
print(y % x)                    # modulus(%)
print(x // y)                   # Floor division operator(//)
print(y // x)                   # Floor division operator(//)

